package packej.cj;

public class Customer {
	int cid;
	String cname;
	long cacno;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public long getCacno() {
		return cacno;
	}
	public void setCacno(long cacno) {
		this.cacno = cacno;
	}
	

}
